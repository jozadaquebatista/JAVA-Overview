
public class ternaryIf
{

  public static void main(String[] args)
  {
  
    int a = 5;
    int b = 16;
    
    if(a > 6 && b > 12)
    {
    
      // execute foo ...
    
    }
    
    if(a > 6 || b > 12)
    {
    
      // execute foo ...
    
    }
    
    /* && and || look if at least one of the sentences are true. */
    
    if(a > 6 & b > 12)
    {
    
      // execute foo ...
    
    }
    
    if(a > 6 | b > 12)
    {
    
      // execute foo ...
    
    }
    
    /* & and | look if both sentences are true.
    *
    *  That's all.
    *
     */
      
  
  }

}